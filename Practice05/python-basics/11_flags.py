# we can add flags when using regular expressions
# re.ASCII      -   [shorthand]: re.A - Returns only ASCII matches	
# re.DEBUG      -                     - Returns debug information	
# re.DOTALL	    -   [shorthand]: re.S - Makes the . character match all characters (including newline character)	
# re.IGNORECASE -   [shorthand]: re.I - Case-insensitive matching	
# re.MULTILINE  -   [shorthand]: re.M - Returns only matches at the beginning of each line
# re.NOFLAG	    -                     - Specifies that no flag is set for this pattern	
# re.UNICODE	-   [shorthand]: re.U -	Returns Unicode matches. This is default from Python 3	
# re.VERBOSE	-   [shorthand]: re.X - Allows whitespaces and comments inside patterns. Makes the pattern more readable