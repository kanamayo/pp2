len()       # returns number of items
sum()       # adds all numbers in list
min()       # finds smallest value in list
max()       # finds largest value in list
map()       # applies function to every item in list
filter()    # keeps items that return True based on condition
enumerate() # returns (index, value) pairs for each item
zip()       # combines two lists into pairs
sorted()    # returns a new list sorted in ascending order